#include <unistd.h>

int			main(int argc, char **argv)
{
	int			i;
	int			j;

	if (argc == 3)
	{
		j = 0;
		i = -1;
		while (argv[2][++i] != '\0')
		{
			if (argv[2][i] == argv[1][j])
				j++;
		}
		write(1, ((argv[1][j] == '\0') ? "1" : "0"), 1);
	}
	write(1, "\n", 1);
	return (0);
}
